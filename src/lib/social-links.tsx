const PLATFORM_INFO: Array<{ key: string; label: string }> = [
  { key: "social_facebook", label: "Facebook" },
  { key: "social_instagram", label: "Instagram" },
  { key: "social_youtube", label: "YouTube" },
  { key: "social_whatsapp", label: "WhatsApp" },
  { key: "social_twitter", label: "Twitter / X" },
  { key: "social_telegram", label: "Telegram" },
  { key: "social_tiktok", label: "TikTok" },
  { key: "social_linkedin", label: "LinkedIn" },
  { key: "social_pinterest", label: "Pinterest" },
  { key: "social_threads", label: "Threads" },
];

let cachedLinks: SocialLink[] | null = null;
let cacheTime = 0;
const CACHE_TTL = 5 * 60 * 1000; // 5 minutes

export async function getSocialLinks(): Promise<SocialLink[]> {
  // Check memory cache
  if (cachedLinks && Date.now() - cacheTime < CACHE_TTL) {
    return cachedLinks;
  }

  try {
    const res = await fetch(
      `${CMS_API}/api/v1/sites/${CMS_SITE_ID}/settings`,
      {
        headers: { "X-API-Key": CMS_API_KEY },
        next: { revalidate: 300, tags: ["social-links"] },
      }
    );
    const json = await res.json();
    if (!json.success) return [];

    const settings = json.data?.settings ?? {};
    const links: SocialLink[] = [];

    for (const platform of PLATFORM_INFO) {
      const url = settings[platform.key];
      if (url && url.trim()) {
        const platformName = platform.key.replace("social_", "");
        let fullUrl = url.trim();

        // WhatsApp: convert number to wa.me link
        if (platformName === "whatsapp") {
          const num = url.replace(/[^+\d]/g, "");
          fullUrl = num.startsWith("88") ? `https://wa.me/${num}` : `https://wa.me/88${num}`;
        }

        links.push({
          key: platformName,
          url: fullUrl,
          label: platform.label,
          icon: ICONS[platformName] || "",
        });
      }
    }

    cachedLinks = links;
    cacheTime = Date.now();
    return links;
  } catch {
    return cachedLinks ?? [];
  }
}

// Synchronous version for client components (uses cached data)
export function getSocialLinksSync(): SocialLink[] {
  return cachedLinks ?? [];
}

// SocialIcon component — renders SVG from icon path
export function SocialIcon({ path, className }: { path: string; className?: string }) {
  return (
    <svg className={className} viewBox="0 0 24 24" fill="currentColor" aria-hidden="true">
      <path d={path} />
    </svg>
  );
}
